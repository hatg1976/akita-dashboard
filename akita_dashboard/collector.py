import os
import requests
import pandas as pd
from dotenv import load_dotenv

load_dotenv()

ESTAT_API_KEY = os.getenv("ESTAT_API_KEY", "")
ESTAT_BASE_URL = "https://api.e-stat.go.jp/rest/3.0/app/json"

# 秋田県コード
AKITA_PREF_CODE = "05"

# 主要統計の統計表ID
# e-Stat で検索して取得したID
STAT_TABLE_IDS = {
    "人口推計": "0000010101",          # 人口推計（都道府県別）
    "住民基本台帳": "0000020201",      # 住民基本台帳人口移動報告
}


def fetch_estat(endpoint: str, params: dict) -> dict:
    """e-Stat API の共通リクエスト処理"""
    params["appId"] = ESTAT_API_KEY
    params["lang"] = "J"
    url = f"{ESTAT_BASE_URL}/{endpoint}"
    response = requests.get(url, params=params, timeout=30)
    response.raise_for_status()
    return response.json()


def search_stats(keyword: str, area_code: str = AKITA_PREF_CODE) -> pd.DataFrame:
    """統計表を検索する"""
    params = {
        "searchWord": keyword,
        "statsField": "02",  # 人口・世帯
    }
    data = fetch_estat("getStatsList", params)
    tables = data.get("GET_STATS_LIST", {}).get("DATALIST_INF", {}).get("TABLE_INF", [])
    if isinstance(tables, dict):
        tables = [tables]
    rows = []
    for t in tables:
        rows.append({
            "統計表ID": t.get("@id"),
            "統計名": t.get("STAT_NAME", {}).get("$", ""),
            "表題": t.get("TITLE", {}).get("$", ""),
            "調査年月": t.get("SURVEY_DATE", ""),
        })
    return pd.DataFrame(rows)


def fetch_population_data() -> pd.DataFrame:
    """
    国勢調査の秋田県人口データをe-Statから取得する
    統計表ID: 0003410379 (国勢調査 都道府県別人口)
    """
    params = {
        "statsDataId": "0003410379",
        "cdArea": f"{AKITA_PREF_CODE}000",
        "metaGetFlg": "Y",
        "cntGetFlg": "N",
    }
    try:
        data = fetch_estat("getStatsData", params)
        values = data["GET_STATS_DATA"]["STATISTICAL_DATA"]["DATA_INF"]["VALUE"]
        if isinstance(values, dict):
            values = [values]
        df = pd.DataFrame(values)
        df.columns = [c.lstrip("@") for c in df.columns]
        return df
    except Exception as e:
        return pd.DataFrame({"エラー": [str(e)]})


