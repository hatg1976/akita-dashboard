_POLICY_CACHE_PATH = Path(__file__).parent / "data" / "policy_cache" / "policy_data.json"

def _load_policy_cache() -> dict:
    """JSONキャッシュを読み込む。存在しなければ空dictを返す"""
    if _POLICY_CACHE_PATH.exists():
        try:
            return json.loads(_POLICY_CACHE_PATH.read_text(encoding="utf-8"))
        except Exception:
            pass
    return {}

def get_policy_last_updated() -> str:
    """政策データの最終更新日を返す"""
    cache = _load_policy_cache()
    return cache.get("last_updated", "未取得")

def get_policy_kpi_note() -> str:
    """政策KPIの注記を返す"""
    cache = _load_policy_cache()
    return cache.get("kpi_note", "")

ESTAT_API_KEY = os.getenv("ESTAT_API_KEY", "")
ESTAT_BASE_URL = "https://api.e-stat.go.jp/rest/3.0/app/json"

# 秋田県コード
AKITA_PREF_CODE = "05"

# 主要統計の統計表ID
