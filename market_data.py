def calc_market_size(expenditure_per_hh: int, households: int) -> int:
    """推計市場規模を計算する（円）"""
    return expenditure_per_hh * households


def calc_share(own_sales: int, market_size: int) -> float:
    """市場シェア（%）を計算する"""
    if market_size <= 0:
        return 0.0
    return own_sales / market_size * 100


