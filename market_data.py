def get_trend(item_name: str) -> dict:
    """品目の年次推移データを返す {年: [...], 支出: [...]}"""
    # 品目名の表記ゆれに対応（括弧・スラッシュを除いた短縮名でもマッチ）
    years = EXPENDITURE_TREND["年"]
    # 完全一致
    if item_name in EXPENDITURE_TREND:
        return {"年": years, "支出（円）": EXPENDITURE_TREND[item_name]}
    # 部分一致（品目名が長い場合の前方一致）
    for key in EXPENDITURE_TREND:
        if key != "年" and (item_name.startswith(key) or key.startswith(item_name)):
            return {"年": years, "支出（円）": EXPENDITURE_TREND[key]}
    return {}


def get_all_trend_items() -> list:
    """推移データがある品目一覧を返す"""
    return [k for k in EXPENDITURE_TREND.keys() if k != "年"]
